    private void initWindChart() {
        List<Double> windSpeedData = new ArrayList<>();
        List<Double> windDirectionData = new ArrayList<>();

        // Generate random data for the wind chart
        for (int i = 0; i < 10; i++) {
            windSpeedData.add(Math.random() * 10);
            windDirectionData.add(Math.random() * 360);
        }

        // Create chart
        XYChart chart = new XYChart(600, 400);
        chart.setTitle("Wind Chart");
        chart.setXAxisTitle("Wind Speed");
        chart.setYAxisTitle("Wind Direction");

        // Add series to the chart
        chart.addSeries("Wind Data", windSpeedData, windDirectionData).setMarker(SeriesMarkers.CIRCLE);

        // Customize the chart style
        chart.getStyler().setLegendPosition(Styler.LegendPosition.InsideNE);

        // Create Panel
        XChartPanel<XYChart> chartPanel = new XChartPanel<>(chart);
        chartPanel.setPreferredSize(new Dimension(560, 370));
        jPanelWindChart.add(chartPanel);
    }

