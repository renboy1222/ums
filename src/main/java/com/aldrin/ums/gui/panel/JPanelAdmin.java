    private TaskSeriesCollection createDataset() {
        TaskSeriesCollection dataset = new TaskSeriesCollection();

        // Add tasks to the dataset
        TaskSeries taskSeries1 = new TaskSeries("Orders");
        taskSeries1.add(new Task("Task 1", date(1, 1), date(1, 5)));
        taskSeries1.add(new Task("Task 2", date(2, 1), date(2, 10)));
        taskSeries1.add(new Task("Task 3", date(3, 5), date(3, 15)));

        TaskSeries taskSeries2 = new TaskSeries("Sold");
        taskSeries2.add(new Task("Task A", date(1, 3), date(1, 8)));
        taskSeries2.add(new Task("Task B", date(2, 6), date(2, 12)));

        dataset.add(taskSeries1);
        dataset.add(taskSeries2);

        return dataset;
    }

